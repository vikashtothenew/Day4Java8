package Java8;

public class FunctionInterface {
    static void staticAdd(int a,int b){
        System.out.println(a+b);
    }
     void instanceAdd(int a,int b){
        System.out.println(a+b);
    }
    static void staticSubtract(int a,int b){
        System.out.println(a-b);
    }
    void instanceSubtract(int a,int b){
        System.out.println(a-b);
    }

}
